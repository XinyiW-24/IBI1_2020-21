def readseq(filename):#extract DNA sequence inthe file
    flie=open(filename,'r')#open the file
    lines=flie.readlines()#read lines in the file
    seq=''
    for i in range(len(lines)):#read lines one by one
        if lines[i].startswith('>'):#find the name
            continue
        else:
            seq+=lines[i]#find the DNA sequence
    seq=seq[:-1]#delete '\n'
    return seq

